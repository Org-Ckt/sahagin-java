package org.sahagin.report;

public class ReportScreenCapture {

    // relative path from HTML report file
    private String path;
    private String ttId;

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }

    public String getTtId() {
        return ttId;
    }

    public void setTtId(String ttId) {
        this.ttId = ttId;
    }

}
